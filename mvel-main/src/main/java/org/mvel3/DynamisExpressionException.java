package org.mvel3;

public class DynamisExpressionException extends RuntimeException {

    public DynamisExpressionException(String message) {
        super(message);
    }

    public DynamisExpressionException(String message, Throwable cause) {
        super(message, cause);
    }

    public DynamisExpressionException(Throwable cause) {
        super(cause);
    }
}
